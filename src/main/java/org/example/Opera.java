package org.example;

public class Opera {

  private String name;
  private String composer;
  private int year;

  public String getName() {
    return name;
  }

  public String getComposer() {
    return composer;
  }

  public int getYear() {
    return year;
  }

  public void setName(String name) {
    this.name = name;
  }

  public void setComposer(String composer) {
    this.composer = composer;
  }

  public void setYear(int year) {
    this.year = year;
  }
}
